/************************
 * Operator Precedence
 */

var now = 2024;
var yearAtikur = 2009;
var fullAge = 14;
// Multiple Operators
var isFullAge = now - yearAtikur >= fullAge;
console.log(isFullAge);

var ageAtikur = now - yearAtikur;
var ageRahim = 30;

// Grouping
var average = (ageAtikur + ageRahim) / 2;
console.log(average);

var x, y;
x = y = (2 + 3) * 6 / 2;

console.log(x, y);

x *= 10;
console.log(x);

x--;
console.log(x);





